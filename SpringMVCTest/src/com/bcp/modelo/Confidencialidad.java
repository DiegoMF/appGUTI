package com.bcp.modelo;

public class Confidencialidad {
	private int idConfidencialidad;
	private String decripcionConfidencialidad;
	public int getIdConfidencialidad() {
		return idConfidencialidad;
	}
	public void setIdConfidencialidad(int idConfidencialidad) {
		this.idConfidencialidad = idConfidencialidad;
	}
	public String getDecripcionConfidencialidad() {
		return decripcionConfidencialidad;
	}
	public void setDecripcionConfidencialidad(String decripcionConfidencialidad) {
		this.decripcionConfidencialidad = decripcionConfidencialidad;
	}
	
	

}
