package com.bcp.modelo;

public class Privacidad {
	private int idPrivacidad;
	private String descripcionPrivacidad;
	public int getIdPrivacidad() {
		return idPrivacidad;
	}
	public void setIdPrivacidad(int idPrivacidad) {
		this.idPrivacidad = idPrivacidad;
	}
	public String getDescripcionPrivacidad() {
		return descripcionPrivacidad;
	}
	public void setDescripcionPrivacidad(String descripcionPrivacidad) {
		this.descripcionPrivacidad = descripcionPrivacidad;
	}
	
	
}
