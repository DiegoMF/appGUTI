package com.bcp.modelo;

public class AccesosAplicacion {
	private int idAccesosAplicacion;
	private String descripcionAccesosAplicacion;
	
	public int getIdAccesosAplicacion() {
		return idAccesosAplicacion;
	}
	public void setIdAccesosAplicacion(int idAccesosAplicacion) {
		this.idAccesosAplicacion = idAccesosAplicacion;
	}
	public String getDescripcionAccesosAplicacion() {
		return descripcionAccesosAplicacion;
	}
	public void setDescripcionAccesosAplicacion(
			String descripcionAccesosAplicacion) {
		this.descripcionAccesosAplicacion = descripcionAccesosAplicacion;
	}
	
	
}
