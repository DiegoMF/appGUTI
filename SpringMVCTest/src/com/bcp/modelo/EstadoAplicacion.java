package com.bcp.modelo;

public class EstadoAplicacion {
	private int idEstadoAplicacion;
	private String descripcionEstadoAplicacion;
	
	public int getIdEstadoAplicacion() {
		return idEstadoAplicacion;
	}
	public void setIdEstadoAplicacion(int idEstadoAplicacion) {
		this.idEstadoAplicacion = idEstadoAplicacion;
	}
	public String getDescripcionEstadoAplicacion() {
		return descripcionEstadoAplicacion;
	}
	public void setDescripcionEstadoAplicacion(
			String descripcionEstadoAplicacion) {
		this.descripcionEstadoAplicacion = descripcionEstadoAplicacion;
	}
	
	
}
