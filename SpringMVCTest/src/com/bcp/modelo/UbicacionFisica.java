package com.bcp.modelo;

public class UbicacionFisica {
	private int idUbicacionFisica;
	private String descripcionUbicacionFisica;
	public int getIdUbicacionFisica() {
		return idUbicacionFisica;
	}
	public void setIdUbicacionFisica(int idUbicacionFisica) {
		this.idUbicacionFisica = idUbicacionFisica;
	}
	public String getDescripcionUbicacionFisica() {
		return descripcionUbicacionFisica;
	}
	public void setDescripcionUbicacionFisica(String descripcionUbicacion) {
		this.descripcionUbicacionFisica = descripcionUbicacion;
	}
	

}
